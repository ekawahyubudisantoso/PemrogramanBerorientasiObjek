public class kubus{
    public static void main(String[] args){
        int sisi, luas, keliling;

        sisi        = 10;
        luas        = 6 * sisi * sisi;
        keliling    = 12 * sisi;

        System.out.println("\n----- Menghitung Luas Kubus -----\n");
        System.out.println("Sisi\t\t: "+sisi);
        System.out.println("Luas\t\t: "+luas);
        System.out.println("\n----- Menghitung Keliling Kubus -----\n");
        System.out.println("Sisi\t\t: "+sisi);
        System.out.println("Keliling\t: "+keliling);
    }
}